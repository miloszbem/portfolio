const GLUE_WORDS = [
	'a', 'i', 'o', 'u', 'w', 'z',
	'do', 'od', 'na', 'po', 'ze', 'we', 'za', 'by', 'ku',
	'czy', 'nie', 'już', 'że', 'aż', 'iż', 'tak', 'ta', 'to', 'co', 'który', 'są', 'być', 'mnie', 'jest', 'ci', 'cię',
]

const GLUE_PATTERN = new RegExp(`(?<=^|\\s)(${GLUE_WORDS.join('|')})\\s+`, 'gi')

function noOrphans(text: string): string {
	let result = text.replace(GLUE_PATTERN, '$1 ')
	result = result.replace(/(\d)\s(\d)/g, '$1 $2')
	const lastSpace = result.lastIndexOf(' ')
	if (lastSpace !== -1) {
		result = result.slice(0, lastSpace) + ' ' + result.slice(lastSpace + 1)
	}
	return result
}

function deepNoOrphans<T>(value: T): T {
	if (typeof value === 'string') return noOrphans(value) as unknown as T
	if (Array.isArray(value)) return value.map((v) => deepNoOrphans(v)) as unknown as T
	if (value !== null && typeof value === 'object') {
		const result: Record<string, unknown> = {}
		for (const key of Object.keys(value as Record<string, unknown>)) {
			result[key] = deepNoOrphans((value as Record<string, unknown>)[key])
		}
		return result as T
	}
	return value
}

